/*программа 4
(Данная программа проверяет наличие
 отрицательных чисел в массиве A размером n и возвращает 1,
 если такое число присутствует, и 0 в противном случае.)*/

#include <stdio.h>

int checkNegative(int A[], int n) {
    int s = 0;
    
    for (int i = 0; i < n; i++) {
        if (A[i] < 0) {
            s = 1;
            break;
        }
    }
    
    return s;
}

int main() {
    int A[] = {1, 2, 3, -4, 5}; // пример массива
    int n = sizeof(A) / sizeof(A[0]);
    
    int result = checkNegative(A, n);
    
    if (result == 1) {
        printf("Отрицательное число найдено в массиве A.\n");
    } else {
        printf("Отрицательных чисел в массиве A нет.\n");
    }
    
    return 0;
}


